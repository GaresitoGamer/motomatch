/**
 * Helper para saltar el bloqueo de hotlinking y optimizar imágenes
 * utilizando el proxy de Google (googleusercontent.com) que es inmune
 * a bloqueadores de publicidad, firewalls y políticas de CORS locales.
 */
export function getProxyImageUrl(url, width = 600, height = 300, fit = "cover") {
  if (!url) return "";
  if (url.startsWith("data:")) return url; // base64 images
  
  // Si la imagen ya viene de Unsplash, no la pasamos por el proxy de Google
  // ya que Unsplash permite hotlinking directo de forma ilimitada y sin CORS
  if (url.includes("images.unsplash.com") || url.includes("upload.wikimedia.org")) {
    return url;
  }
  
  const googleProxy = "https://images1-focus-opensocial.googleusercontent.com/gadgets/proxy?container=focus&refresh=2592000";
  const encodedUrl = encodeURIComponent(url);
  
  // Si especificamos ancho, añadimos parámetros de redimensionamiento de Google
  if (width) {
    return `${googleProxy}&resize_w=${width}&url=${encodedUrl}`;
  }
  return `${googleProxy}&url=${encodedUrl}`;
}
